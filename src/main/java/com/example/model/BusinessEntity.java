package com.example.model;

public class BusinessEntity {
    private String date;
    private String summaryPeriod;
    private String merchantNumber;
    private String businessName;
    private String mailingAddress;
    private int subMIDNumber;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getSummaryPeriod() {
        return summaryPeriod;
    }

    public void setSummaryPeriod(String summaryPeriod) {
        this.summaryPeriod = summaryPeriod;
    }

    public String getMerchantNumber() {
        return merchantNumber;
    }

    public void setMerchantNumber(String merchantNumber) {
        this.merchantNumber = merchantNumber;
    }

    public String getBusinessName() {
        return businessName;
    }

    public void setBusinessName(String businessName) {
        this.businessName = businessName;
    }

    public String getMailingAddress() {
        return mailingAddress;
    }

    public void setMailingAddress(String mailingAddress) {
        this.mailingAddress = mailingAddress;
    }

    public int getSubMIDNumber() {
        return subMIDNumber;
    }

    public void setSubMIDNumber(int subMIDNumber) {
        this.subMIDNumber = subMIDNumber;
    }
}

























